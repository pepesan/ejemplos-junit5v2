package example;

public class Calculator {

    public int add(int first, int second) {
        return first+ second;
    }

    public int multiply(int i, int j) {
        return i*j;
    }

    public int divide(int i, int j) {
        return i/j;
    }

    public int subtract(int i, int j) {
        return i-j;
    }
}
